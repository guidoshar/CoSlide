export function generateHTML(data: Presentation, styleId: string = "storytelling"): string {
  const preset = getStyleById(styleId);
  const C = preset.colors;
  const total = data.slides.length;

  const slidesHtml = data.slides.map((slide, i) => {
    if (i === 0) return renderCoverSlide(slide, i, total);
    if (i === total - 1 && total > 1) return renderEndingSlide(slide, i, total);
    return renderContentSlide(slide, i, total);
  }).join("\n");

  return `<!DOCTYPE html>
<html lang="zh-CN">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>${escapeHtml(data.title)}</title>
<style>${buildCSS(C)}</style>
</head>
<body>
${slidesHtml}
<div class="nav-bar">
  <button class="nav-btn" onclick="go(-1)">&larr;</button>
  ${data.slides.map((_, i) => `<div class="nav-dot" data-i="${i}" onclick="goTo(${i})"></div>`).join("")}
  <button class="nav-btn" onclick="go(1)">&rarr;</button>
</div>
<script>
  let cur = 0;
  const slides = document.querySelectorAll('.slide');
  const dots = document.querySelectorAll('.nav-dot');
  function show() {
    slides.forEach((s, i) => s.classList.toggle('active', i === cur));
    dots.forEach((d, i) => d.classList.toggle('active', i === cur));
  }
  function go(d) { cur = Math.max(0, Math.min(slides.length - 1, cur + d)); show(); }
  function goTo(i) { cur = i; show(); }
  show();
  document.addEventListener('keydown', e => {
    if (e.key === 'ArrowRight' || e.key === ' ') go(1);
    if (e.key === 'ArrowLeft') go(-1);
  });
</script>
</body>
</html>`;
}

export function downloadHTML(data: Presentation, styleId: string = "storytelling") {
  const html = generateHTML(data, styleId);
  const blob = new Blob([html], { type: "text/html;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  const sanitized = data.title.replace(/[^a-zA-Z0-9\u4e00-\u9fff\s\-_]/g, "").trim() || "presentation";
  a.download = `${sanitized}.html`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
