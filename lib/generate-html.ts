import type { Presentation } from "./types";
import { getStyleById, type StyleColors } from "./presets";

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function renderCoverSlide(slide: { title: string; body: string[] }, idx: number, total: number): string {
  const subtitle = slide.body?.length ? slide.body.map(escapeHtml).join("  &middot;  ") : "";
  return `
    <section class="slide slide-cover" data-index="${idx}">
      <div class="cover-dark">
        <div class="cover-accent-bar"></div>
        <h1>${escapeHtml(slide.title)}</h1>
      </div>
      <div class="cover-light">
        ${subtitle ? `<p class="cover-subtitle">${subtitle}</p>` : ""}
        <div class="cover-deco-line"></div>
        <span class="watermark">Generated by CoSlide</span>
      </div>
      <span class="page-num">01 / ${String(total).padStart(2, "0")}</span>
    </section>`;
}

function renderContentSlide(slide: { title: string; body: string[] }, idx: number, total: number): string {
  const bullets = slide.body?.map((b) => `<li><span class="bullet-marker">&#9632;</span> ${escapeHtml(b)}</li>`).join("\n        ") || "";
  return `
    <section class="slide slide-content" data-index="${idx}">
      <div class="left-bar"></div>
      <div class="page-badge">${String(idx + 1).padStart(2, "0")}</div>
      <div class="content-area">
        <h2>${escapeHtml(slide.title)}</h2>
        <div class="title-underline"></div>
        <ul>${bullets}</ul>
      </div>
      <div class="slide-footer">
        <span class="page-num">${String(idx + 1).padStart(2, "0")} / ${String(total).padStart(2, "0")}</span>
      </div>
    </section>`;
}

function renderEndingSlide(slide: { title: string; body: string[] }, idx: number, total: number): string {
  const bodyLines = slide.body?.map((b) => `<p>${escapeHtml(b)}</p>`).join("\n        ") || "";
  return `
    <section class="slide slide-ending" data-index="${idx}">
      <div class="ending-accent-top"></div>
      <h1>${escapeHtml(slide.title)}</h1>
      <div class="ending-deco-line"></div>
      <div class="ending-body">${bodyLines}</div>
      <div class="ending-accent-bottom"></div>
      <span class="watermark-end">Generated by CoSlide</span>
      <span class="page-num-end">${String(total).padStart(2, "0")} / ${String(total).padStart(2, "0")}</span>
    </section>`;
}

